"use server";

import { z } from "zod";
// A tool that both validates and defines the shape of your data (with types).

// import database connection
import postgres from "postgres";
const sql = postgres(process.env.POSTGRES_URL!, { ssl: "require" });

import { revalidatePath } from "next/cache";
import { redirect } from "next/navigation";

const FormSchema = z.object({
  id: z.string(),
  customerId: z.string(),

  // coerce means:
  // “Try to convert the value into this type before validating it.”
  amount: z.coerce.number(),

  // enum means:
  // “Only allow these exact values — nothing else.”
  status: z.enum(["pending", "paid"]),
  date: z.string(),
});

// “Create a new schema based on the original, but remove some fields.”
// removed the id and date fields because they are generated by the database, so we don't need to include them in the form data.
const CreateInvoice = FormSchema.omit({ id: true, date: true });

export async function createInvoice(formData: FormData) {
  const { customerId, amount, status } = CreateInvoice.parse({
    customerId: formData.get("customerId"),
    amount: formData.get("amount"),
    status: formData.get("status"),
  });

  console.log(formData.get("customerId"));

  const amountInCents = amount * 100;
  const date = new Date().toISOString().split("T")[0];

  try {
    await sql`
    INSERT INTO invoices (customer_id, amount, status, date)
    VALUES (${customerId}, ${amountInCents}, ${status}, ${date})
  `;
  } catch (error) {
    console.error("Error creating invoice:", error);
  }

  // Once the database has been updated, the /dashboard/invoices path will be revalidated, and fresh data will be fetched from the server.
  revalidatePath("/dashboard/invoices");

  // After the invoice is created, you want to redirect the user back to the invoices page. You can do this with the redirect function from Next.js:
  redirect("/dashboard/invoices");
}
